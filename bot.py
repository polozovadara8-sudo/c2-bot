    app.add_handler(CommandHandler("new", new_chat))
    app.add_handler(CommandHandler("end", end_chat))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    print("C2 запущен...")
    app.run_polling()
